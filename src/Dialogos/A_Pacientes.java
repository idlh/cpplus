    public void filtro() {
    filtro = filtrar.getText();
    String v = (String) jComboBox1.getSelectedItem();
    int columna = 3;
    if(v.equals("Nombres")){
        columna = 3;
    } else if (v.equals("Apellidos")){
        columna = 4;
    } 
    trsfiltro.setRowFilter(RowFilter.regexFilter(filtrar.getText(), columna));
    }  
