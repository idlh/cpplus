    public static DefaultTableModel modelo;
     public void CrearModelo(){
         try {
             modelo = (new DefaultTableModel(
                null, new String [] {
                "id","TD","Identificación",
                "Nombres","Apellidos","Fecha Nac","Contratante"}){
                Class[] types = new Class [] {
                java.lang.String.class,
                java.lang.String.class,
                java.lang.String.class,
                java.lang.String.class,
                java.lang.String.class,
		java.lang.String.class,
                java.lang.String.class
                };
                boolean[] canEdit = new boolean [] {
                false,false,false,false,false,false,false
                };
                @Override
                public Class getColumnClass(int columnIndex) {
                   return types [columnIndex];
                }
                @Override
                public boolean isCellEditable(int rowIndex, int colIndex){
                   return canEdit [colIndex];
                }
            });
           tabla.setModel(modelo);
           tabla.getTableHeader().setReorderingAllowed(false);
           tabla.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
           tabla.getColumnModel().getColumn(1).setPreferredWidth(30);
           tabla.getColumnModel().getColumn(1).setMaxWidth(30);
           tabla.getColumnModel().getColumn(5).setPreferredWidth(70);
           tabla.getColumnModel().getColumn(5).setMaxWidth(70);
//           tabla.getColumnModel().getColumn(5).setPreferredWidth(70);
//           tabla.getColumnModel().getColumn(5).setMaxWidth(70);
           Funciones.ocultarColumnas(tabla, new int[] {0});
           } catch (Exception e) {
             JOptionPane.showMessageDialog(null,e.toString()+"error2");
            }     
        } 
     public void cargar_pacientes(){
         try {
             Object O[]=null;
             List<InfoPaciente>pacientes;
             pacientes=control_pacientes.find_pacientes();
             for (int i = 0; i < pacientes.size(); i++) {
              modelo.addRow(O);   
              modelo.setValueAt(pacientes.get(i), i, 0);
              modelo.setValueAt(pacientes.get(i).getTipoDoc(), i, 1);
              modelo.setValueAt(pacientes.get(i).getNumDoc(), i, 2);
              modelo.setValueAt(pacientes.get(i).getNombre1()+ " " +pacientes.get(i).getNombre2(), i, 3);
              modelo.setValueAt(pacientes.get(i).getApellido1()+ " " +pacientes.get(i).getApellido2(), i, 4);
              modelo.setValueAt(Funciones.Formatear_Fecha_object(pacientes.get(i).getFechaNacimiento()), i, 5);
              modelo.setValueAt(pacientes.get(i).getContratante().getNombreEntidad(), i, 6);
             }
         } catch (Exception e) {
             JOptionPane.showMessageDialog(this, e.getMessage());
         }
     }
     
     
     
     
     
     
     
     
