     public static DefaultTableModel modelo;
     public void CrearModelo(){
         try {
             modelo = (new DefaultTableModel(
                null, new String [] {
                "id","Codigo","Entidad"}){
                Class[] types = new Class [] {
                java.lang.String.class,
                java.lang.String.class,
                java.lang.String.class
                };
                boolean[] canEdit = new boolean [] {
                false,false,false
                };
                @Override
                public Class getColumnClass(int columnIndex) {
                   return types [columnIndex];
                }
                @Override
                public boolean isCellEditable(int rowIndex, int colIndex){
                   return canEdit [colIndex];
                }
            });
           tabla.setModel(modelo);
           tabla.getTableHeader().setReorderingAllowed(false);
           tabla.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
           tabla.getColumnModel().getColumn(1).setPreferredWidth(70);
           tabla.getColumnModel().getColumn(1).setMaxWidth(70);
           Funciones.ocultarColumnas(tabla, new int[] {0});
           } catch (Exception e) {
             JOptionPane.showMessageDialog(null,e.toString()+"error2");
            }     
        } 
     public void Cargar_Entidades(){
         try {
            Object E[]=null;
            List<InfoEntidades>entidad;
            entidad=Control_Entidades.find_entidades();
             for (int i = 0; i < entidad.size(); i++) {
                modelo.addRow(E);
                modelo.setValueAt(entidad.get(i), i, 0);
                modelo.setValueAt(entidad.get(i).getCodigoEntidad(), i, 1);
                modelo.setValueAt(entidad.get(i).getNombreEntidad(), i, 2);
             }
         } catch (Exception e) {
             JOptionPane.showMessageDialog(this, e.getMessage());
         }
     }
     
