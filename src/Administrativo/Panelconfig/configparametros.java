    private void jButton1MouseReleased(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton1MouseReleased
        final Creacionpara param = new Creacionpara((Frame) SwingUtilities.getWindowAncestor(this), true);
        param.jButton2.addActionListener(new ActionListener() {

            @Override
            public void actionPerformed(ActionEvent e) {
                if (param.jTextField1.getText().equals("")) {
                    JOptionPane.showMessageDialog(null, "Debe escoger un parametro para configurar");
                } else {
                    if (param.jTextField2.getText().equals("")) {
                        JOptionPane.showMessageDialog(null, "Debe añadir un valor al parametro a configurar");
                    } else {
                        sav.nuevaconfig(param.jLabel1.getText(), String.valueOf(param.jComboBox1.getSelectedIndex()), param.jTextField2.getText(), param.jTextField3.getText());
                        jTable1.removeAll();
                        tabla();
                        param.dispose();
                    }
                }
            }
        });
        param.setLocationRelativeTo(null);
        param.setVisible(true);
    }//GEN-LAST:event_jButton1MouseReleased

    private void jButton2MouseReleased(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton2MouseReleased
        if (jTable1.getSelectedRow() > -1) {
            final Creacionpara param = new Creacionpara((Frame) SwingUtilities.getWindowAncestor(this), true);
            Object data[][] = Funciones.RetornarDatos(sav.cargarpramen(String.valueOf(jTable1.getValueAt(jTable1.getSelectedRow(), 0))));
            param.jTextField1.setText(data[0][3].toString());
            param.jComboBox1.setSelectedIndex(Integer.parseInt(data[0][4].toString()));
            param.jTextField2.setText(data[0][5].toString());
            param.jLabel1.setText(data[0][1].toString());
            param.jTextField3.setText(data[0][6].toString());
            param.jButton2.addActionListener(new ActionListener() {

                @Override
                public void actionPerformed(ActionEvent e) {
                    sav.modifparam(String.valueOf(jTable1.getValueAt(jTable1.getSelectedRow(), 0)), String.valueOf(param.jComboBox1.getSelectedIndex()), param.jTextField2.getText(), param.jTextField3.getText());
                    jTable1.removeAll();
                    tabla();
                    param.dispose();
                }
            });
            param.setLocationRelativeTo(null);
            param.setVisible(true);
        } else {
            JOptionPane.showMessageDialog(null, "Debe seleccionar un parametro");
        }
    }//GEN-LAST:event_jButton2MouseReleased

    private void jButton3MouseReleased(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton3MouseReleased
        String mensaje = "¿Esta Seguro(a) de querer deshabilitar este Parametro? ";
        int entrada = JOptionPane.showConfirmDialog(null, mensaje, "Confirmar finalizacion", JOptionPane.YES_NO_OPTION);
        if (entrada == 0) {
            sav.deshabilitarparam(String.valueOf(jTable1.getValueAt(jTable1.getSelectedRow(), 0)));
            jTable1.removeAll();
            tabla();
        }
    }//GEN-LAST:event_jButton3MouseReleased

